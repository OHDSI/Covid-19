    SELECT concept_id
    FROM @target_database_schema.concept_phenotypes
    WHERE phenotype = 'Influenza-like illness'
        AND criteria = 'inclusion'
        AND concept_id IS NOT NULL
    )
AND ca1.descendant_concept_id != c2.concept_id

